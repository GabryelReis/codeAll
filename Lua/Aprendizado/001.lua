-- Variáveis

texto = "Texto." -- Uma variável (algo que varia)
-- que recebe um texto

print(texto) -- Imprimo o conteúdo da variável

texto = 10+20+30 -- Modifico a variável

print(texto) 

texto = "Novamente um texto."

print(texto)

complexo = (10 * (83 - 50)) % 23 - (80 / 2)
quarenta_e_dois = 42

print(complexo)

print(complexo .. " " .. texto) -- .. serve para 
-- concatenar (juntar) dois conteúdos

print(complexo + quarenta_e_dois)
-- o símbolo de + serve para somar dois conteúdos, e
-- não juntá-los.
